function choiceDirection(){
    let choiceDirection = document.querySelector('#choice')
    if(choiceDirection.value == 'backend'){
        backend_direction.classList.remove('_active')
        frontend_direction.classList.add('_active')
    }
    else if(choiceDirection.value == 'frontend'){
        frontend_direction.classList.remove('_active')
        backend_direction.classList.add('_active')
    }
}

